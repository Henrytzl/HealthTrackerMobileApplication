package com.example.healthtracker.healthymeal.FoodListRecycleView;

public class RecycleViewFoodList {
    int carb, fat, kcal, noOfUnit, protein, sugar;
    String foodID, foodName, userID;

    public String getFoodID(){
        return foodID;
    }

    public String getFoodName(){
        return foodName;
    }

    public String getUserID(){
        return userID;
    }

    public int getCarb(){
        return carb;
    }

    public int getFat(){
        return fat;
    }

    public int getKcal(){
        return kcal;
    }

    public int getNoOfUnit(){
        return noOfUnit;
    }

    public int getProtein(){
        return protein;
    }

    public int getSugar() {
        return sugar;
    }
}
